namespace Editor
{
    /// <summary>
    /// Тип текстового поля
    /// </summary>
    public enum TextInputType
    {
        /// <summary>
        /// Обычный текст
        /// </summary>
        String,

        /// <summary>
        /// Целое число
        /// </summary>
        Integer,

        /// <summary>
        /// Число с плавающей точкой
        /// </summary>
        Float,

        /// <summary>
        /// Пароль (скрытые символы)
        /// </summary>
        Password
    }
}
